let x = Math.floor(Math.random() * 100 + 1);
let y = Math.floor(Math.random() * 50 + 1);

let add = x + y;
let sub = x - y;
let mul = x * y;
let div = x / y;
let rm = x % y;

console.log(`${x} + ${y} = ${add}`);
console.log(`${x} - ${y} = ${sub}`);
console.log(`${x} * ${y} = ${mul}`);
console.log(`${x} / ${y} = ${div}`);
console.log(`${x} % ${y} = ${rm}`);
